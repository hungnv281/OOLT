package customer;

public class Object {
    private int width;
    private int height;
    private int length;

    public int getWidth()
    {
        return this.width;
    }
    public void setWidth(int width)
    {
        this.width = width;
    }
    public int getHeight()
    {
        return this.height;
    }
    public void setHeight(int height)
    {
        this.height= height;
    }
    public int getLength()
    {
        return this.length;
    }
    private void setLength(int length)
    {
        this.length = length;
    }
}
